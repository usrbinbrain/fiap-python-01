#!/usr/bin/env python3

# Declarando o valor da variavel.
minha_var = 10

# Exibindo o valor da variavel.
print(minha_var)

# Exibindo o valor da variavel multiplicado 100 vezes.
print(minha_var * 100)

# Exibindo o valor da variavel somado com mais 25.
print(minha_var + 25)

# Exibindo o valor da variavel menos 1.
print(minha_var - 1)

# Exibindo o valor da variavel dividido por 2.
print(minha_var / 2)

