#!/usr/bin/env python3

# Declarando um lista com valores numericos.
minha_lista = [1, 2, 3, 4, 5]

# O loop for imprime o valor de cada um dos item da lista "minha_lista".
for item in minha_lista:
    print(item)


# O loop for imprime o valor de cada um dos item da lista "minha_lista" somados com mais 10.
for item in minha_lista:
    print(item + 10)
